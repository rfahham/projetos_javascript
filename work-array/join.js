let bikes = ["Yamaha", "Honda", "Kawazaki", "Ducati", "Harley-Davidson", "BMW"];

// Imprime separado por virgulas
console.log(bikes.join());

// Imprime sem espaço entre as palavras
console.log(bikes.join(""));

// Imprime separado por hífen
console.log(bikes.join("-"));

// Imprime pulando linha
console.log(bikes.join("\n"));

// Retorna o objeto x do array
console.log(bikes[1]);
